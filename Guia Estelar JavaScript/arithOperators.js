/*

    Operadores aritméticos

*/

// Multiplicação
console.log(3 * 4);

// Divisão
console.log(26 / 4);

// Soma
console.log(3 + 4);

// Subtração
console.log(14 - 4);

// Resto
let remainder = 11 % 4;
console.log(remainder);

// Incremento
let increment = 0;
increment++;
console.log(increment);
console.log(++increment);

// Decremento
let decrement = 10;
decrement--;
console.log(decrement);
console.log(--decrement);

// Exponencial
console.log(3 ** 3);